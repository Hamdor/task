#include "taski/all.hpp"

#include <iostream>

int main() {
  taski::scheduler<taski::stealing, 4> scheduler;
  auto future = scheduler.enqueue([](auto i) {
    return i + 1;
  }, 42);
  std::cout << "future: " << future.get() << std::endl;
}
